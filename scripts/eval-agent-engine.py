#!/usr/bin/env python3
"""Run ADK evaluation against the JAK Swarm gateway agent.

This script uses the ADK Python SDK programmatic API instead of the CLI,
because the CLI expects a specific package structure that conflicts with
our module layout.

Usage:
    GEMINI_API_KEY=your-key python scripts/eval-agent-engine.py

Outputs:
    qa/_generated/adk-eval-results.json  — Machine-readable eval scores
    qa/adk-eval-results.md                — Human-readable eval report
"""

import asyncio
import json
import os
import sys
from datetime import datetime, timezone
from pathlib import Path

REPO_ROOT = Path(__file__).resolve().parent.parent
EVAL_DIR = REPO_ROOT / "packages" / "adk" / "eval"
EVAL_SETS_DIR = REPO_ROOT / "qa" / "eval-sets"
OUTPUT_DIR = REPO_ROOT / "qa" / "_generated"
REPORT_DIR = REPO_ROOT / "qa"

# Ensure the agent module is importable
sys.path.insert(0, str(EVAL_DIR))

os.environ.setdefault("GEMINI_API_KEY", "")


async def run_eval():
    """Run the ADK evaluation using the programmatic API."""
    from google.adk.evaluation.agent_evaluator import AgentEvaluator

    # Import the agent directly
    from __init__ import root_agent

    eval_set_path = str(EVAL_SETS_DIR / "jak-gateway-eval-set.json")
    config_path = str(EVAL_SETS_DIR / "jak-gateway-eval-config.json")

    print(f"Agent: {root_agent.name} (model: {root_agent.model})")
    print(f"Eval set: {eval_set_path}")
    print(f"Config: {config_path}")
    print(f"Running evaluation...")

    results = await AgentEvaluator.evaluate(
        agent_module=str(EVAL_DIR),
        eval_dataset_file_path=eval_set_path,
        config_file_path=config_path,
    )

    return results


async def main():
    """Main entry point."""
    generated_at = datetime.now(timezone.utc).isoformat()

    if not os.environ.get("GEMINI_API_KEY"):
        print("ERROR: GEMINI_API_KEY environment variable is required.")
        print("Set it with: export GEMINI_API_KEY=your-key")
        sys.exit(1)

    try:
        results = await run_eval()
    except Exception as e:
        print(f"ERROR: Evaluation failed: {e}")
        import traceback
        traceback.print_exc()

        error_output = {
            "generated_at": generated_at,
            "error": str(e),
            "error_type": type(e).__name__,
            "traceback": traceback.format_exc(),
        }

        OUTPUT_DIR.mkdir(parents=True, exist_ok=True)
        with open(OUTPUT_DIR / "adk-eval-results.json", "w") as f:
            json.dump(error_output, f, indent=2)

        print(f"\nError results written to: {OUTPUT_DIR / 'adk-eval-results.json'}")
        sys.exit(1)

    # Serialize results
    if hasattr(results, "model_dump"):
        results_dict = results.model_dump()
    elif hasattr(results, "__dict__"):
        results_dict = {k: str(v) for k, v in results.__dict__.items()}
    else:
        results_dict = {"raw": str(results)}

    output = {
        "generated_at": generated_at,
        "eval_set": "jak_gateway_eval_v1",
        "agent": "JAKSwarmGateway",
        "model": "gemini-2.5-flash",
        "results": results_dict,
    }

    # Write JSON output
    OUTPUT_DIR.mkdir(parents=True, exist_ok=True)
    with open(OUTPUT_DIR / "adk-eval-results.json", "w") as f:
        json.dump(output, f, indent=2, default=str)

    # Write markdown report
    report_lines = [
        f"# ADK Evaluation Results — jak-swarm-gateway — {generated_at}",
        "",
        f"Generated at: `{generated_at}`  ",
        f"Eval set: `jak_gateway_eval_v1`  ",
        f"Agent: `JAKSwarmGateway`  ",
        f"Model: `gemini-2.5-flash`  ",
        "",
        "## Methodology",
        "",
        "This evaluation uses Google ADK's `AgentEvaluator` against the JAK Swarm gateway agent.",
        "The agent uses `gemini-2.5-flash` with `GOOGLE_SEARCH` grounding.",
        "",
        "Criteria:",
        "- `tool_trajectory_avg_score`: Whether the agent called the expected tools",
        "- `response_match_score`: ROUGE-1 similarity to reference responses",
        "- `rubric_based_final_response_quality_v1`: LLM-judged response quality",
        "",
        "## Results",
        "",
        "```json",
        json.dumps(results_dict, indent=2, default=str)[:5000],
        "```",
        "",
        "---",
        "",
        "_This report is generated by the ADK AgentEvaluator._",
    ]

    with open(REPORT_DIR / "adk-eval-results.md", "w") as f:
        f.write("\n".join(report_lines))

    print(f"\n✅ Evaluation complete!")
    print(f"   JSON: {OUTPUT_DIR / 'adk-eval-results.json'}")
    print(f"   Report: {REPORT_DIR / 'adk-eval-results.md'}")


if __name__ == "__main__":
    asyncio.run(main())